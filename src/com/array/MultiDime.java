package com.array;

public class MultiDime {
	public static void main(String[] args) {
		
		
		
		int arr[][]= {{2,3,4},{6,4,3},{2,5,9}};
		
		
		for(int i=0; i<arr.length; i++ ) {
			
			for(int j=0; j<arr.length; j++) {
				System.out.println(arr[i][j]);
				
			}
			
			System.out.println();
			
		}
	}

}
